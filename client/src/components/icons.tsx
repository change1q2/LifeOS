export const MOOD_ICONS: Record<number, LucideIcon> = {
  1: Frown,
  2: Meh,
  3: Meh,
  4: Smile,
  5: Heart,
};

export function ModuleIcon({ emoji, className, style }: { emoji: string; className?: string; style?: React.CSSProperties }) {
  const Icon = MODULE_ICONS[emoji] || LayoutDashboard;
  return <Icon className={className} style={style} />;
}

export function MoodIcon({ score, className }: { score: number; className?: string }) {
  const Icon = MOOD_ICONS[score] || Meh;
  return <Icon className={className} />;
}

